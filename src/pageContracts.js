function projectInput(key, kind, required, purpose, options = {}) {
  const input = { key, kind, required: Boolean(required), purpose };
  if (options.audit) input.audit = options.audit;
  return input;
}

function output(key, kind, purpose, consumers = []) {
  return { key, kind, purpose, consumers };
}

function contract({
  workflowStep = null,
  standaloneInputs,
  projectInputs = [],
  outputs,
  readiness,
  downstream = [],
  notes = []
}) {
  return {
    workflowStep,
    standaloneInputs,
    projectInputs,
    outputs,
    readiness,
    downstream,
    notes
  };
}

function normalizeHref(href) {
  return String(href || '').split('/').pop();
}

function routeLabel(href) {
  const normalized = normalizeHref(href);
  const route = NAV_ROUTES.find(item => item.href === normalized);
  if (route?.label) return route.label;
  return normalized.replace(/\.html$/i, '').replace(/[-_]/g, ' ');
}

