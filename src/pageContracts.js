export function getPageContract(href) {
  const normalized = normalizeHref(href);
  return PAGE_CONTRACTS_BY_HREF[normalized] || null;
}

export function getPageContractReadiness(href) {
  const normalized = normalizeHref(href);
  const pageContract = getPageContract(normalized);
  if (!pageContract) return null;
  const downstream = pageContract.downstream.map(item => ({
    href: item,
    label: routeLabel(item)
  }));
  return {
    href: normalized,
    label: routeLabel(normalized),
    terms: READINESS_VOCABULARY,
    readyWhen: pageContract.readiness.readyWhen,
    blockers: [...pageContract.readiness.blockers],
    downstream,
    downstreamText: downstream.length
      ? downstream.map(item => item.label).join(', ')
      : 'No downstream handoff declared.'
  };
}

export function getPageContractReadinessMessages(href) {
  const readiness = getPageContractReadiness(href);
  if (!readiness) return null;
  return {
    ready: `${READINESS_VOCABULARY.ready}: ${readiness.readyWhen}`,
    missingInputs: `${READINESS_VOCABULARY.missingInputs}: ${readiness.blockers.join('; ')}`,
    downstreamHandoff: `${READINESS_VOCABULARY.downstreamHandoff}: ${readiness.downstreamText}`
  };
}

export function getPageContractsForSection(section) {
  return NAV_ROUTES
    .filter(route => route.section === section)
    .map(route => ({ route, contract: getPageContract(route.href) }))
    .filter(item => item.contract);
}

export function getPageContractCoverage(routes = NAV_ROUTES) {
  const scopedRoutes = routes.filter(route => CONTRACT_SECTIONS.has(route.section));
  const routeHrefs = new Set(scopedRoutes.map(route => route.href));
  const contractHrefs = Object.keys(PAGE_CONTRACTS_BY_HREF);
  return {
    totalRoutes: scopedRoutes.length,
    totalContracts: contractHrefs.length,
    missing: scopedRoutes.filter(route => !PAGE_CONTRACTS_BY_HREF[route.href]).map(route => route.href),
    extra: contractHrefs.filter(href => !routeHrefs.has(href)),
    routes: scopedRoutes
  };
}
